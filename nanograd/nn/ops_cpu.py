import numpy as np


def add_forward(a, b):
    r"""Numpy array addition"""
    return a + b

def neg_forward(a):
    r"""Numpy array negation"""
    return -a

def mul_forward(a, b):
    r"""Numpy array multiplication"""
    return a * b

def pow_forward(a, power):
    r"""Numpy array power"""
    return a ** power